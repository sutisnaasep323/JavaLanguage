package Udemy;

public class Casting {

    public static void main(String[] args) {

        //casting dari terbesar ke kecil (manual)
        double a = 4.5;
        int c = (int) a; //0.5
        System.out.println(c);

        // casting dari terkecil ke besar (otomatis)
        int b = 5;
        double d = b;
        System.out.println(d);
    }

}
