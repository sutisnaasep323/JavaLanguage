package Udemy.PZN.S1_JavaDasar;

public class S1_10_OperatorPerbandingan {
    public static void main(String[] args) {

        int a = 100, b = 100;

        System.out.println(a > b);
        System.out.println(a >= b);
        System.out.println(a <= b);
        System.out.println(a < b);
        System.out.println(a == b);
        System.out.println(a != b);

    }
}
