package Udemy;

public class IncrementDecrement {

    public static void main(String[] args) {

        /*
        a++ => a = a + 1 atau a += 1
         */

        int a = 9;
        a++; //increment
        System.out.println(a);

        a += 5;
        System.out.println(a);

        a--;
        System.out.println(a);

        a -= 4;
        System.out.println(a);

        a *= 5;
        System.out.println(a);

        a /= 10;
        System.out.println(a);

    }

}
