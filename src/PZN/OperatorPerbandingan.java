package PZN;

public class OperatorPerbandingan {

    public static void main(String[] args) {

        int a = 100, b = 100;

        System.out.println(a > b);
        System.out.println(a >= b);
        System.out.println(a <= b);
        System.out.println(a < b);
        System.out.println(a == b);
        System.out.println(a != b);

    }

}
