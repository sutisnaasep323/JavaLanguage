package KT;

public class VariabelAssigmentDeklarasi_08 {
    public static void main(String[] args) {

        //Variabel
        int a = 10;
        System.out.println("Nilai a = " + a);
        a = 15; //merubah nilainya
        System.out.println("Nilai a baru = " + a);

        //Delarasi
        int b;
        b = 7; //menaruh nilai setelah di deklare

        System.out.println("Nilai b = " + b);
    }
}
