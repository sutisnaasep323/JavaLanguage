package KT;

public class OperatorAssigments_13 {

    public static void main(String[] args) {

        //Operator Assignments, adalah operasi "="

        //Assignments Penjumlahan
        int a = 1;
        a += 10; // a = a + 10. jika kita mengikuti metode di operator aritmatika
        System.out.printf("nilai a = %d\n", a);

        //Assignments Pengurangan
        int b = 100;
        b -= 10;
        System.out.printf("nilai b = %d\n", b);

        //Assignments Perkalian
        int c = 10;
        c *= 10;
        System.out.printf("nilai c = %d\n",c);

        //Assignments Pembagian
        int d = 100;
        d /= 10;
        System.out.printf("nilai d = %d\n",d);

        //Assignments modulus
        int e = 20;
        e %= 7;
        System.out.printf("nilai e = %d\n",e);
    }

}
